# FOR LOOP
# Syntex of For Loop
# for(name in vector){execute until condition is TRUE}

for(x in 1:8){print(x^2)}

for(x in c(2,4,6,8)){print(x^2)}

# WHILE LOOP
# Syntex of While Loop
# for(Condition){execute until condition is TRUE}

i<-1
while(i<10){
  i<-i^2
  print(i)
  i<-i+1
 }