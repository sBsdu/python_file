v <- c(1,2,3,4)
t <- c(2,3,4,5)

print("Addition")
print(v+t)

print("Multiplication")
print(v*t)

print("Division")
print(v/t)

print("Substraction")
print(v-t)

print("Quotient")
print(v%%t)

print("Remainder")
print(v%/%t)

# Some Relational Operators are :  < , > , == , != , <= , >=
print(v>t)


